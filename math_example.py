import math

# Exemplo de uso dos métodos mais utilizados da biblioteca math

# Valor absoluto
print("Valor absoluto de -5:", math.fabs(-5))

# Fatorial
print("Fatorial de 5:", math.factorial(5))

# Potência
print("2 elevado a 3:", math.pow(2, 3))

# Raiz quadrada
print("Raiz quadrada de 16:", math.sqrt(16))

# Logaritmo natural (base e)
print("Logaritmo natural de 2.71828:", math.log(math.e))

# Logaritmo base 10
print("Logaritmo base 10 de 100:", math.log10(100))

# Seno de 90 graus (convertido para radianos)
print("Seno de 90 graus:", math.sin(math.radians(90)))

# Cosseno de 0 graus (convertido para radianos)
print("Cosseno de 0 graus:", math.cos(math.radians(0)))

# Tangente de 45 graus (convertido para radianos)
print("Tangente de 45 graus:", math.tan(math.radians(45)))

# Valor de pi
print("Valor de pi:", math.pi)

# Valor de e
print("Valor de e:", math.e)